create table Deal_Auction (
	AUC_NUMBER		 NUMBER primary key , 		--글번호 
	AUC_SUBJECT 	 VARCHAR2(50) , 				--글제목
	AUC_CONTENT 	 VARCHAR2(4000), 			--글내용
	USER_ID			 VARCHAR2(60) references MEMBER_INFO(USER_ID)
					on delete cascade , 		--아이디
	AUC_DATE 		 DATE, 						--경매기간/		
